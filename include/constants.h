#ifndef CONSTANTS_H
#define CONSTANTS_H

#define ONE_USEC   (24UL)
#define ONE_MSEC   (ONE_USEC * 1000)
#define ONE_SECOND (ONE_MSEC * 1000)
#define ONE_MINUTE (ONE_SECOND * 60)
#define ONE_HOUR   (ONE_MINUTE * 60)
#define ONE_DAY    (ONE_HOUR * 24)

#endif /* CONSTANTS_H */
